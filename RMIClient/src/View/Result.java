    public void getChalenge()//Ham nhan loi thach dau, cac lenh sau chuc nang tuong ung voi phan thach dau trong UserHome
    {
        ClientControl cc = new ClientControl();
        threadGetChalenge = new Thread(new Runnable() {
            @Override
            public void run() {
                User thachdau = null;
                while (true)
                {
                    thachdau = cc.remoteContinueGetChalenge(me);
                    if (thachdau!=null)
                    {
                        int click = JOptionPane.showConfirmDialog(null, thachdau.getName()+" muốn đấu tiếp?");
                        if (click==JOptionPane.YES_OPTION) 
                        {
                            if (cc.remoteCheckStatus(thachdau) == 2)
                            {
                                cc.remoteContinueResponChalenge(thachdau, me);
                                enemy = thachdau;
                                cc.remoteMakeTest(thachdau, me);
                                Wait w1 = new Wait(me, enemy, home);
                                w1.setVisible(true);
                                closeWindow();
                                threadGetResponChalenge.stop();
                                checkOut.stop();
                                threadGetChalenge.stop();
                            }
                        }
                    }
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(UserHome.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });
        threadGetChalenge.start();
    }
    
