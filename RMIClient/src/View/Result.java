    public void loadResult() //Kiem tra tra ra ket qua
    {
        matchMe = (Match) result.get(0);
        matchEnemy = (Match) result.get(1);
        if (matchMe.getPoint()==10)
        {
            jLabel1.setText("Chúc mừng! Bạn đã chiến thắng!");
        }
        else
        {
            if (matchMe.getPoint()==5)
            {
                jLabel1.setText("Hòa!");
            }
            else
            {
                if (matchMe.getPoint()==0)
                {
                    jLabel1.setText("Đừng buồn! Thua keo này ta bày keo khác!");
                }
            }
        }
        
        jLabel6.setText(enemy.getName());
        
        jLabel10.setText(String.valueOf(matchMe.getWrong()));
        jLabel11.setText(String.valueOf(matchMe.getTime()));
        jLabel12.setText(String.valueOf(matchMe.getPoint()));
        
        jLabel13.setText(String.valueOf(matchEnemy.getWrong()));
        jLabel14.setText(String.valueOf(matchEnemy.getTime()));
        jLabel15.setText(String.valueOf(matchEnemy.getPoint()));
    }
    
    public void CheckEnemyOut()//Luong kiem tra doi thu da thoat khoi tran dau hay chua (1s 1 lan)
    {
        ClientControl cc = new ClientControl();
        checkOut = new Thread(new Runnable() {
            @Override
            public void run() {
                while(true)
                {
                    if (cc.remoteCheckStatus(enemy)!=2)
                    {
                        showMessage("Đối thủ đã thoát!");
                        cc.remoteImOnline(me);
                        checkOut.stop();
                    }
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Result.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        });
        checkOut.start();
    }
    
